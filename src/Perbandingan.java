public class Perbandingan {
    public static void main(String[] args) {
        // output true or false
        int a = 100;
        long b = 9;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
